/*---------------------------------------------------------------------*/
/*    T3dFontRaster::T3dFontRaster ...                                 */
/*---------------------------------------------------------------------*/
T3dFontRaster::T3dFontRaster()
  :cFontOffset(0),
   cBegin( 0 ),
   cEnd( 0 ),
   cWidth( 0 ),
   cHeight( 0 )
{
}

/*---------------------------------------------------------------------*/
/*    T3dFontRaster::T3dFontRaster ...                                 */
/*---------------------------------------------------------------------*/
T3dFontRaster::T3dFontRaster( GLubyte pRaster[][13], int pW, int pH, int pBegin, int pEnd )
  : cFontOffset(0),
    cBegin( 0 ),
    cEnd( 0 ),
    cWidth( pW ),
    cHeight( pH )
{
  init( pRaster, pW, pH, pBegin, pEnd );
}
/*---------------------------------------------------------------------*/
/*    T3dFontRaster::~T3dFontRaster ...                                */
/*---------------------------------------------------------------------*/

T3dFontRaster::~T3dFontRaster()
{
  if( cFontOffset )
    glDeleteLists( cFontOffset, cEnd+1 );
}

/*---------------------------------------------------------------------*/
/*    T3dFontRaster::init ...                                          */
/*---------------------------------------------------------------------*/

void
T3dFontRaster::init(  GLubyte pRaster[][13], int pW, int pH, int pBegin, int pEnd )
{
  cBegin = pBegin;
  cEnd   = pEnd;
  cWidth = pW;
  cHeight = pH;

  glPixelStorei( GL_UNPACK_ALIGNMENT, 1 );

  cFontOffset = glGenLists(cEnd+1);

  for(GLuint i = pBegin; i < (GLuint)pEnd; i++)
    {
      glNewList( i + cFontOffset, GL_COMPILE );
      glBitmap( cWidth, cHeight, 0.0, 2.0, getMaxWidth(), 0.0, ((GLubyte*)pRaster[i-pBegin]) );
      glEndList();
    }

  //	std::cout << "w1:" << cHeight << std::endl;
}
/*---------------------------------------------------------------------*/
/*    T3dFontRaster::display ...                                       */
/*---------------------------------------------------------------------*/

void
T3dFontRaster::display( char c )
{
  glPushAttrib (GL_LIST_BIT);
  glListBase(cFontOffset+1);
  glCallList( c );
  glPopAttrib ();
}
/*---------------------------------------------------------------------*/
/*    T3dFontRaster::display ...                                       */
/*---------------------------------------------------------------------*/

void
T3dFontRaster::display(  const char* pStr, int pSz )
{
  glPushAttrib (GL_LIST_BIT);
  glListBase( cFontOffset);
  if( pSz == -1 )
    pSz = strlen( pStr );

  glCallLists(pSz, GL_UNSIGNED_BYTE, pStr );
  glPopAttrib ();
}
/*---------------------------------------------------------------------*/
/*    T3dFontRaster::displayAt ...                                     */
/*---------------------------------------------------------------------*/

void
T3dFontRaster::displayAt( GLdouble pX, GLdouble pY, GLdouble pZ, const char *pStr, int pSz )
{
  glRasterPos3d( pX, pY, pZ );
  display( pStr, pSz );
}
